package fr.ribardiere.tennis.bean;

public class Club {
	
	private String nom;
	private String numeroClub;
	private String numeroEquipe;
	private String nomResponsable;
	private String telephone;
	private String telephonePortable;
	private String email;
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getNumeroClub() {
		return numeroClub;
	}
	public void setNumeroClub(String numeroClub) {
		this.numeroClub = numeroClub;
	}
	public String getNumeroEquipe() {
		return numeroEquipe;
	}
	public void setNumeroEquipe(String numeroEquipe) {
		this.numeroEquipe = numeroEquipe;
	}
	public String getNomResponsable() {
		return nomResponsable;
	}
	public void setNomResponsable(String nomResponsable) {
		this.nomResponsable = nomResponsable;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getTelephonePortable() {
		return telephonePortable;
	}
	public void setTelephonePortable(String telephonePortable) {
		this.telephonePortable = telephonePortable;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

}
